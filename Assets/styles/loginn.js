// Ambil elemen tombol
const loginButton = document.getElementById("loginButton");

// Tambahkan event listener untuk tombol (opsional, hanya untuk efek visual)
loginButton.addEventListener("click", function() {
    // Anda dapat menambahkan efek visual di sini, misalnya:
    loginButton.classList.add("loading"); // Menambahkan kelas CSS "loading" untuk efek loading
});